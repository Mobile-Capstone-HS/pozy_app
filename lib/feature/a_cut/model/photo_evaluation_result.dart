import 'aesthetic_ensemble_weights.dart';
import 'model_score_detail.dart';

class PhotoEvaluationResult {
  final double finalScore;
  final double technicalScore;
  final double? aestheticScore;
  final double? finalAestheticScore;
  final double? nimaScore;
  final double? rgnetScore;
  final double? alampScore;
  final double? nimaWeight;
  final double? rgnetWeight;
  final double? alampWeight;
  final String verdict;
  final List<String> notes;
  final List<String> warnings;
  final List<ModelScoreDetail> scoreDetails;
  final String? modelVersion;
  final String? fileName;
  final bool usesTechnicalScoreAsFinal;
  /// One-sentence summary generated by the explanation service.
  final String? shortExplanation;
  /// Multi-sentence expert analysis generated by the explanation service.
  final String? detailedExplanation;
  /// Eye-state annotation from Gemini: 'open', 'closed', or 'unknown'.
  /// Null when no Gemini explanation has been produced yet.
  final String? eyeState;
  /// Brief reason for the [eyeState] judgment (Korean, from Gemini).
  final String? eyeStateReason;

  const PhotoEvaluationResult({
    required this.finalScore,
    required this.technicalScore,
    required this.verdict,
    this.aestheticScore,
    this.finalAestheticScore,
    this.nimaScore,
    this.rgnetScore,
    this.alampScore,
    this.nimaWeight,
    this.rgnetWeight,
    this.alampWeight,
    this.notes = const [],
    this.warnings = const [],
    this.scoreDetails = const [],
    this.modelVersion,
    this.fileName,
    this.usesTechnicalScoreAsFinal = false,
    this.shortExplanation,
    this.detailedExplanation,
    this.eyeState,
    this.eyeStateReason,
  });

  factory PhotoEvaluationResult.fromJson(Map<String, dynamic> json) {
    return PhotoEvaluationResult(
      finalScore: (json['final_score'] as num).toDouble(),
      technicalScore: (json['technical_score'] as num).toDouble(),
      aestheticScore: (json['aesthetic_score'] as num?)?.toDouble(),
      finalAestheticScore:
          (json['final_aesthetic_score'] as num?)?.toDouble(),
      nimaScore: (json['nima_score'] as num?)?.toDouble(),
      rgnetScore: (json['rgnet_score'] as num?)?.toDouble(),
      alampScore: (json['alamp_score'] as num?)?.toDouble(),
      nimaWeight: (json['nima_weight'] as num?)?.toDouble(),
      rgnetWeight: (json['rgnet_weight'] as num?)?.toDouble(),
      alampWeight: (json['alamp_weight'] as num?)?.toDouble(),
      verdict: json['verdict'] as String,
      notes: (json['notes'] as List<dynamic>?)?.cast<String>() ?? const [],
      warnings:
          (json['warnings'] as List<dynamic>?)?.cast<String>() ?? const [],
      scoreDetails:
          (json['score_details'] as List<dynamic>?)
              ?.map(
                (entry) => ModelScoreDetail.fromJson(
                  entry as Map<String, dynamic>,
                ),
              )
              .toList() ??
          const [],
      modelVersion: json['model_version'] as String?,
      fileName: json['file_name'] as String?,
      usesTechnicalScoreAsFinal:
          json['uses_technical_score_as_final'] as bool? ?? false,
      shortExplanation: json['short_explanation'] as String?,
      detailedExplanation: json['detailed_explanation'] as String?,
      eyeState: json['eye_state'] as String?,
      eyeStateReason: json['eye_state_reason'] as String?,
    );
  }

  Map<String, dynamic> toJson() => {
        'final_score': finalScore,
        'technical_score': technicalScore,
        if (aestheticScore != null) 'aesthetic_score': aestheticScore,
        if (finalAestheticScore != null)
          'final_aesthetic_score': finalAestheticScore,
        if (nimaScore != null) 'nima_score': nimaScore,
        if (rgnetScore != null) 'rgnet_score': rgnetScore,
        if (alampScore != null) 'alamp_score': alampScore,
        if (nimaWeight != null) 'nima_weight': nimaWeight,
        if (rgnetWeight != null) 'rgnet_weight': rgnetWeight,
        if (alampWeight != null) 'alamp_weight': alampWeight,
        'verdict': verdict,
        'notes': notes,
        'warnings': warnings,
        'score_details': scoreDetails.map((detail) => detail.toJson()).toList(),
        if (modelVersion != null) 'model_version': modelVersion,
        if (fileName != null) 'file_name': fileName,
        'uses_technical_score_as_final': usesTechnicalScoreAsFinal,
        if (shortExplanation != null) 'short_explanation': shortExplanation,
        if (detailedExplanation != null)
          'detailed_explanation': detailedExplanation,
        if (eyeState != null) 'eye_state': eyeState,
        if (eyeStateReason != null) 'eye_state_reason': eyeStateReason,
      };

  factory PhotoEvaluationResult.fromScores({
    required double finalScore,
    required double technicalScore,
    double? aestheticScore,
    double? finalAestheticScore,
    double? nimaScore,
    double? rgnetScore,
    double? alampScore,
    double? nimaWeight,
    double? rgnetWeight,
    double? alampWeight,
    List<String> notes = const [],
    List<String> warnings = const [],
    List<ModelScoreDetail> scoreDetails = const [],
    String? modelVersion,
    String? fileName,
    bool usesTechnicalScoreAsFinal = false,
    String? shortExplanation,
    String? detailedExplanation,
    String? eyeState,
    String? eyeStateReason,
  }) {
    final normalizedFinal = finalScore.clamp(0.0, 1.0).toDouble();
    final normalizedTechnical = technicalScore.clamp(0.0, 1.0).toDouble();
    final normalizedAesthetic =
        (aestheticScore?.clamp(0.0, 1.0) as num?)?.toDouble();
    final normalizedFinalAesthetic =
        ((finalAestheticScore ?? aestheticScore)?.clamp(0.0, 1.0) as num?)
            ?.toDouble();
    final normalizedWeights = nimaWeight != null &&
            rgnetWeight != null &&
            alampWeight != null
        ? AestheticEnsembleWeights(
            nimaWeight: nimaWeight,
            rgnetWeight: rgnetWeight,
            alampWeight: alampWeight,
          )
        : null;

    return PhotoEvaluationResult(
      finalScore: normalizedFinal,
      technicalScore: normalizedTechnical,
      aestheticScore: normalizedAesthetic,
      finalAestheticScore: normalizedFinalAesthetic,
      nimaScore: (nimaScore?.clamp(0.0, 1.0) as num?)?.toDouble(),
      rgnetScore: (rgnetScore?.clamp(0.0, 1.0) as num?)?.toDouble(),
      alampScore: (alampScore?.clamp(0.0, 1.0) as num?)?.toDouble(),
      nimaWeight: normalizedWeights?.nimaWeight,
      rgnetWeight: normalizedWeights?.rgnetWeight,
      alampWeight: normalizedWeights?.alampWeight,
      verdict: _verdictFor(normalizedFinal),
      notes: notes,
      warnings: warnings,
      scoreDetails: scoreDetails,
      modelVersion: modelVersion,
      fileName: fileName,
      usesTechnicalScoreAsFinal: usesTechnicalScoreAsFinal,
      shortExplanation: shortExplanation,
      detailedExplanation: detailedExplanation,
      eyeState: eyeState,
      eyeStateReason: eyeStateReason,
    );
  }

  PhotoEvaluationResult copyWith({
    double? finalScore,
    double? technicalScore,
    double? aestheticScore,
    double? finalAestheticScore,
    double? nimaScore,
    double? rgnetScore,
    double? alampScore,
    double? nimaWeight,
    double? rgnetWeight,
    double? alampWeight,
    String? verdict,
    List<String>? notes,
    List<String>? warnings,
    List<ModelScoreDetail>? scoreDetails,
    String? modelVersion,
    String? fileName,
    bool? usesTechnicalScoreAsFinal,
    String? shortExplanation,
    String? detailedExplanation,
    String? eyeState,
    String? eyeStateReason,
  }) {
    return PhotoEvaluationResult(
      finalScore: finalScore ?? this.finalScore,
      technicalScore: technicalScore ?? this.technicalScore,
      aestheticScore: aestheticScore ?? this.aestheticScore,
      finalAestheticScore: finalAestheticScore ?? this.finalAestheticScore,
      nimaScore: nimaScore ?? this.nimaScore,
      rgnetScore: rgnetScore ?? this.rgnetScore,
      alampScore: alampScore ?? this.alampScore,
      nimaWeight: nimaWeight ?? this.nimaWeight,
      rgnetWeight: rgnetWeight ?? this.rgnetWeight,
      alampWeight: alampWeight ?? this.alampWeight,
      verdict: verdict ?? this.verdict,
      notes: notes ?? this.notes,
      warnings: warnings ?? this.warnings,
      scoreDetails: scoreDetails ?? this.scoreDetails,
      modelVersion: modelVersion ?? this.modelVersion,
      fileName: fileName ?? this.fileName,
      usesTechnicalScoreAsFinal:
          usesTechnicalScoreAsFinal ?? this.usesTechnicalScoreAsFinal,
      shortExplanation: shortExplanation ?? this.shortExplanation,
      detailedExplanation: detailedExplanation ?? this.detailedExplanation,
      eyeState: eyeState ?? this.eyeState,
      eyeStateReason: eyeStateReason ?? this.eyeStateReason,
    );
  }

  int get finalPct => (finalScore * 100).round();

  int get technicalPct => (technicalScore * 100).round();

  bool get hasAestheticScore => aestheticScore != null;

  int? get aestheticPct {
    final score = finalAestheticScore ?? aestheticScore;
    if (score == null) {
      return null;
    }
    return (score * 100).round();
  }

  int? get nimaPct => _pctFromScore(nimaScore);

  int? get rgnetPct => _pctFromScore(rgnetScore);

  int? get alampPct => _pctFromScore(alampScore);

  bool get hasAestheticEnsembleScores =>
      nimaScore != null && rgnetScore != null && alampScore != null;

  bool get hasAnyAestheticEnsembleScore =>
      nimaScore != null || rgnetScore != null || alampScore != null;

  AestheticEnsembleWeights get effectiveAestheticWeights {
    final currentNimaWeight = nimaWeight;
    final currentRgnetWeight = rgnetWeight;
    final currentAlampWeight = alampWeight;
    if (currentNimaWeight == null ||
        currentRgnetWeight == null ||
        currentAlampWeight == null) {
      return AestheticEnsembleWeights.defaults;
    }
    return AestheticEnsembleWeights(
      nimaWeight: currentNimaWeight,
      rgnetWeight: currentRgnetWeight,
      alampWeight: currentAlampWeight,
    );
  }

  PhotoEvaluationResult withAestheticEnsembleWeights(
    AestheticEnsembleWeights weights,
  ) {
    final normalizedWeights = AestheticEnsembleWeights(
      nimaWeight: weights.nimaWeight,
      rgnetWeight: weights.rgnetWeight,
      alampWeight: weights.alampWeight,
    );
    final recomputedAestheticScore = hasAestheticEnsembleScores
        ? normalizedWeights.weightedScore(
            nimaScore: nimaScore!,
            rgnetScore: rgnetScore!,
            alampScore: alampScore!,
          )
        : (finalAestheticScore ?? aestheticScore);
    final usesTechnicalFallback = recomputedAestheticScore == null;
    final recomputedFinalScore = usesTechnicalFallback
        ? technicalScore
        : ((technicalScore * 0.5) + (recomputedAestheticScore * 0.5))
            .clamp(0.0, 1.0)
            .toDouble();

    return PhotoEvaluationResult.fromScores(
      finalScore: recomputedFinalScore,
      technicalScore: technicalScore,
      aestheticScore: recomputedAestheticScore,
      finalAestheticScore: recomputedAestheticScore,
      nimaScore: nimaScore,
      rgnetScore: rgnetScore,
      alampScore: alampScore,
      nimaWeight: normalizedWeights.nimaWeight,
      rgnetWeight: normalizedWeights.rgnetWeight,
      alampWeight: normalizedWeights.alampWeight,
      notes: notes,
      warnings: warnings,
      scoreDetails: _reweightedScoreDetails(normalizedWeights),
      modelVersion: modelVersion,
      fileName: fileName,
      usesTechnicalScoreAsFinal: usesTechnicalFallback,
      shortExplanation: shortExplanation,
      detailedExplanation: detailedExplanation,
      eyeState: eyeState,
      eyeStateReason: eyeStateReason,
    );
  }

  Iterable<ModelScoreDetail> get technicalDetails =>
      scoreDetails.where(
        (detail) => detail.dimension == ModelScoreDimension.technical,
      );

  Iterable<ModelScoreDetail> get aestheticDetails =>
      scoreDetails.where(
        (detail) => detail.dimension == ModelScoreDimension.aesthetic,
      );

  String get qualitySummary {
    switch (verdictLevel) {
      case VerdictLevel.excellent:
        return '대표 컷으로 바로 써도 좋을 만큼 완성도가 높아요.';
      case VerdictLevel.good:
        return '후보 컷으로 올리기 좋은 안정적인 결과예요.';
      case VerdictLevel.average:
        return '무난한 결과지만 더 좋은 컷이 있을 수 있어요.';
      case VerdictLevel.needsWork:
        return '조금만 다시 촬영하면 더 좋아질 수 있어요.';
    }
  }

  String get primaryHint {
    if (notes.isNotEmpty) {
      return notes.first;
    }
    if (warnings.isNotEmpty) {
      return warnings.first;
    }
    return qualitySummary;
  }

  String get evaluationModeLabel {
    if (usesTechnicalScoreAsFinal) {
      return '현재는 온디바이스 품질 평가를 중심으로 요약해요.';
    }
    return '품질과 미적 선호를 함께 반영한 요약 결과예요.';
  }

  VerdictLevel get verdictLevel {
    if (finalScore >= 0.85) return VerdictLevel.excellent;
    if (finalScore >= 0.70) return VerdictLevel.good;
    if (finalScore >= 0.50) return VerdictLevel.average;
    return VerdictLevel.needsWork;
  }

  static String _verdictFor(double score) {
    if (score >= 0.85) return '매우 좋음';
    if (score >= 0.70) return '좋음';
    if (score >= 0.50) return '보통';
    return '아쉬움';
  }

  int? _pctFromScore(double? score) {
    if (score == null) {
      return null;
    }
    return (score * 100).round();
  }

  List<ModelScoreDetail> _reweightedScoreDetails(
    AestheticEnsembleWeights weights,
  ) {
    return scoreDetails.map((detail) {
      switch (detail.id) {
        case 'nima_mobile':
          return detail.copyWith(weight: weights.nimaWeight);
        case 'rgnet_aadb_gpu':
          return detail.copyWith(weight: weights.rgnetWeight);
        case 'alamp_aadb_gpu':
          return detail.copyWith(weight: weights.alampWeight);
        default:
          return detail;
      }
    }).toList(growable: false);
  }
}

enum VerdictLevel { excellent, good, average, needsWork }
